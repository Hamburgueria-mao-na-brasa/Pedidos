create policy "Admin consulta pedidos" on public.mnb_orders for select to authenticated
using (exists (select 1 from public.mnb_admin_profiles a where a.user_id = (select auth.uid())));
drop policy if exists "Admin atualiza pedidos" on public.mnb_orders;
