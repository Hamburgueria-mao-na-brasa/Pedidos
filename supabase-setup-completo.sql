create table if not exists public.mnb_admin_profiles (
  user_id uuid primary key references auth.users(id) on delete cascade,
  email text not null unique,
  created_at timestamptz not null default now()
);

create table if not exists public.mnb_products (
  id text primary key,
  name text not null,
  category text not null default 'Outros',
  tag text not null default '',
  description text not null default '',
  price numeric(10,2) not null default 0 check (price >= 0),
  image text not null default '',
  image_fit text not null default 'cover',
  image_position text not null default 'center',
  has_addons boolean not null default false,
  available boolean not null default true,
  sort_order integer not null default 0,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.mnb_addons (
  id text primary key,
  name text not null unique,
  price numeric(10,2) not null default 0 check (price >= 0),
  available boolean not null default true,
  sort_order integer not null default 0,
  created_at timestamptz not null default now()
);

create table if not exists public.mnb_orders (
  id bigint generated by default as identity primary key,
  protocol text not null unique,
  customer jsonb not null default '{}'::jsonb,
  items jsonb not null default '[]'::jsonb,
  subtotal numeric(10,2) not null default 0,
  delivery_fee numeric(10,2) not null default 0,
  total numeric(10,2) not null default 0,
  status text not null default 'enviado_whatsapp',
  created_at timestamptz not null default now()
);

create table if not exists public.mnb_site_settings (
  id text primary key,
  settings jsonb not null default '{}'::jsonb,
  updated_at timestamptz not null default now()
);

alter table public.mnb_admin_profiles enable row level security;
alter table public.mnb_products enable row level security;
alter table public.mnb_addons enable row level security;
alter table public.mnb_orders enable row level security;
alter table public.mnb_site_settings enable row level security;

grant usage on schema public to anon, authenticated;
grant select on public.mnb_products, public.mnb_addons, public.mnb_site_settings to anon, authenticated;
grant insert on public.mnb_orders to anon, authenticated;
grant select, insert, update, delete on public.mnb_products, public.mnb_addons, public.mnb_orders, public.mnb_site_settings to authenticated;
grant select on public.mnb_admin_profiles to authenticated;
grant usage, select on sequence public.mnb_orders_id_seq to anon, authenticated;

drop policy if exists "Produtos públicos disponíveis" on public.mnb_products;
create policy "Produtos públicos disponíveis" on public.mnb_products for select to anon
using (available = true);
drop policy if exists "Produtos visíveis para usuários" on public.mnb_products;
create policy "Produtos visíveis para usuários" on public.mnb_products for select to authenticated
using (available = true or exists (select 1 from public.mnb_admin_profiles a where a.user_id = (select auth.uid())));
drop policy if exists "Admin gerencia produtos" on public.mnb_products;
create policy "Admin gerencia produtos" on public.mnb_products for all to authenticated
using (exists (select 1 from public.mnb_admin_profiles a where a.user_id = (select auth.uid())))
with check (exists (select 1 from public.mnb_admin_profiles a where a.user_id = (select auth.uid())));

drop policy if exists "Adicionais públicos disponíveis" on public.mnb_addons;
create policy "Adicionais públicos disponíveis" on public.mnb_addons for select to anon
using (available = true);
drop policy if exists "Adicionais visíveis para usuários" on public.mnb_addons;
create policy "Adicionais visíveis para usuários" on public.mnb_addons for select to authenticated
using (available = true or exists (select 1 from public.mnb_admin_profiles a where a.user_id = (select auth.uid())));
